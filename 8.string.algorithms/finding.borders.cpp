int main(void) {
    string input;
    cin >> input;

    int n = input.length();
    vector<int> z(n);

    for (int i = 1, l = 0, r = 0; i < n; ++i) {
        if (i <= r)
            z[i] = min (r - i + 1, z[i - l]);
        while (i + z[i] < n && input[z[i]] == input[i + z[i]])
            ++z[i];
        if (i + z[i] - 1 > r)
            l = i, r = i + z[i] - 1;
    }

    vector<int> result;

    for (int i = n - 1; i > 0; i--) {
        if (z[i] == n - i) {
            result.push_back(z[i]);
        }
    }

    sort(result.begin(), result.end());
    for (auto x : result) {
        cout << x << " ";
    }
    cout << endl;

    return 0;
}
