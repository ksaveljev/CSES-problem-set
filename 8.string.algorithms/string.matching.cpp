int main(void) {
    string input, pattern;
    cin >> input >> pattern;

    string s = pattern + "#" + input;
    int n = s.length();
    vector<int> z(n);

    for (int i = 1, l = 0, r = 0; i < n; ++i) {
        if (i <= r)
            z[i] = min (r - i + 1, z[i - l]);
        while (i + z[i] < n && s[z[i]] == s[i + z[i]])
            ++z[i];
        if (i + z[i] - 1 > r)
            l = i, r = i + z[i] - 1;
    }

    int result = 0;

    loop (n) {
        if (z[i] == pattern.size()) {
            result++;
        }
    }

    cout << result << endl;

    return 0;
}
